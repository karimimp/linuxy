# pip cheat sheet

# install a package
pip install PACKAGE_NAME

# uninstall a package
pip uninstall PACKAGE_NAME

# update a package
pip install --upgrade PACKAGE_NAME

# list all packages
pip list

# list outdated packages
pip list --outdated

# show package info
pip show PACKAGE_NAME

# upgrade pip
pip install --upgrade pip

# show installed packages in requirements format
pip freeze
